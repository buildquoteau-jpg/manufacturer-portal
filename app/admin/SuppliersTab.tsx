'use client'

import { useEffect, useState } from 'react'
import { supabase } from '@/lib/supabase/client'

const ADMIN_PASSWORD = process.env.NEXT_PUBLIC_ADMIN_PASSWORD || 'buildquote2026'

type Manufacturer = {
  id: string; name: string; slug: string; logo_url: string | null; description: string | null; website_url: string | null
}
type System = {
  id: string; name: string; product_code: string; category: string; subcategory: string | null; sort_order: number
}
type EmbedWidgetSystem = {
  system_id: string; systems: { name: string; product_code: string; manufacturer_id: string }
}
type EmbedWidget = {
  id: string; public_token: string; status: string; created_at: string; embed_widget_systems: EmbedWidgetSystem[]
}
type Supplier = {
  id: string; name: string; slug: string; suburb: string | null; state: string | null; address: string | null
  website_url: string | null; email: string | null; phone: string | null
  manager_name: string | null; it_name: string | null; it_email: string | null
  embed_widgets: EmbedWidget[]
}
type Step = 1 | 2 | 3 | 4

const AU_STATES = ['WA', 'VIC', 'NSW', 'QLD', 'SA', 'TAS', 'ACT', 'NT']
const CATEGORIES = ['Decking', 'Cladding', 'Screening & Fencing']

function slugify(s: string) {
  return s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')
}
function buildEmbedCode(token: string, origin: string) {
  return `<iframe src="${origin}/widget/${token}" width="100%" height="800" frameborder="0" style="border:none;border-radius:12px;"></iframe>`
}

